        if (rdioM.isSelected()) {
            gender = "M";
        } else if (rdioF.isSelected()) {
            gender = "F";
        } else {
            JOptionPane.showMessageDialog(rootPane, "Select a Gender");
        }

        String email = txtEmail.getText();

        try {
            String queryUpdate = "UPDATE supplier SET name = '" + name + "', telephone = '" + telephone + "', address = '" + address + "', gender = '" + gender + "', email = '" + email + "' WHERE supplierID = " + supID;
            statement.executeUpdate(queryUpdate);
            JOptionPane.showMessageDialog(rootPane, "record updated");
            model.setRowCount(0);
            getDataToTable();
        } catch (SQLException exsql) {
            JOptionPane.showMessageDialog(rootPane, exsql);
        }
