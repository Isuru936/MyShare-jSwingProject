    public void fetchDataFromDB() {
        
        //connection
        //statement 
        //resultset
        
        
        String url = "jdbc:mysql://localhost:3306/mysharedb";
        String userName = "root";
        String password = "";
        try {
            con = DriverManager.getConnection(url, userName, password);
            statement = con.createStatement();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, ex);
        }
    }

    public void getDataToTable() {
        String query = "SELECT * FROM supplier";
        try {
            ResultSet resultSet = statement.executeQuery(query); //saves the entire in the resultset
            
            while(resultSet.next()){
                int ID = resultSet.getInt("supplierID");  //vales from DB table
                String name =  resultSet.getString("name");
                String telephone = resultSet.getString("telephone");
                String address = resultSet.getString("address");
                String gender = resultSet.getString("gender");
                String email = resultSet.getString("email");
                
                model.addRow(new Object[]{ID, name, telephone, address, gender, email});  //sends values to UI table
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, ex);
        }
    }
