    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        String name = txtName.getText();
        String telephone = txtTele.getText();
        String address  = txtAddress.getText();
        String gender = "";
        if(rdioM.isSelected()){
            gender = "M";
        } else if (rdioF.isSelected()){
            gender = "F";
        } else{
            JOptionPane.showMessageDialog(rootPane, "select a gender");
        }
        String email = txtEmail.getText();
        
        try{
            String query = "INSERT INTO supplier(name, telephone, address, gender, email) VALUES ('" + name + "','" + telephone + "','" + address + "','" + gender + "','" + email+ "')";
            statement.executeUpdate(query); //add values to the DB
//            control.addSupplier(name, telephone, address, gender, email, orders);
            model.setRowCount(0); //clears the data in the table
            getDataToTable();
        } catch (SQLException ex){
            JOptionPane.showMessageDialog(rootPane, ex);
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void tblSupplierMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSupplierMouseClicked
        // TODO add your handling code here:
        int selectedRow = tblSupplier.getSelectedRow();

        txtName.setText(model.getValueAt(selectedRow, 1).toString());
        txtTele.setText(model.getValueAt(selectedRow, 2).toString());
        txtAddress.setText(model.getValueAt(selectedRow, 3).toString());
        String gender = model.getValueAt(selectedRow, 4).toString();
        if (gender.equalsIgnoreCase("M")) {
            rdioM.setSelected(true);
        } else if (gender.equalsIgnoreCase("F")) {
            rdioF.setSelected(true);
        }
        txtEmail.setText(model.getValueAt(selectedRow, 5).toString());
    }//GEN-LAST:event_tblSupplierMouseClicked

    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        // TODO add your handling code here:
        try {
            int selectedRow = tblSupplier.getSelectedRow();
            String supID = model.getValueAt(selectedRow, 0).toString();
            String queryDelete = "DELETE FROM supplier WHERE supplierID = " + supID;
            statement.executeUpdate(queryDelete);
            JOptionPane.showMessageDialog(rootPane, "record deleted");
            model.setRowCount(0);
            getDataToTable();
        } catch (SQLException exsql) {
            JOptionPane.showMessageDialog(rootPane, exsql);
        }
    }//GEN-LAST:event_btnRemoveActionPerformed

